# https://www.acmicpc.net/problem/1958

a = input()
b = input()
c = input()

ls = [list([0]*(len(c)+1) for _ in range(len(b)+1)) for _ in range(len(a)+1)]

for i in range(1, len(a)+1):
    for j in range(1, len(b)+1):
        for k in range(1, len(c)+1):
            if a[i-1] == b[j-1] == c[k-1]:
                ls[i][j][k] = ls[i-1][j-1][k-1]+1
            else:
                ls[i][j][k] = max(ls[i-1][j][k], ls[i][j-1][k], ls[i][j][k-1])

print(ls[len(a)][len(b)][len(c)])

